package chap01;

public class CharToCode {
	
	public static void main(String[] args) {
		char ch = 'A';
		int intCode= 'B';
		System.out.printf("%c = %d \n",ch, intCode);
		
		char characterC = 'a';
		int charCode = (int)characterC;
		System.out.printf("%c = %d",characterC, charCode);
		
	}

}
